    private boolean validarCampos(){
        if (txtName2.getText().isEmpty() || txtLastName2.getText().isEmpty() ||txtNacionality2.getText().isEmpty()) {
            return false;
        }
        return true;
    }
    
    private void limparCampos(){
        txtName2.setText("");
        txtLastName2.setText("");
        txtNacionality2.setText("");
        idSelecionado = -1;
        
    }
    
    
    
    private void listarPessoa() {
        DefaultTableModel model = (DefaultTableModel) tabelaPessoa.getModel();
        model.setRowCount(0);
        
        List<Pessoa> lista = dao.listar();
        
        for(Pessoa a : lista) {
            model.addRow(new Object[]{
                
                a.getId(),
                a.getNome(),
                a.getSobrenome(),
                a.getNacionalidade()
            
            });
        }
    }

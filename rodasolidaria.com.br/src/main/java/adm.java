    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        if(idSelecionado == -1){
            JOptionPane.showMessageDialog(this, "Selecione na tabela");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Deseja excluir?");
        if (confirm == JOptionPane.YES_OPTION) {

            dao.excluir(idSelecionado);
            limparCampos();
            listarPessoa();
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tabelaPessoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaPessoaMouseClicked
       int linha = tabelaPessoa.getSelectedRow();

    idSelecionado = (int) tabelaPessoa.getValueAt(linha, 0);

    txtName2.setText(tabelaPessoa.getValueAt(linha, 1).toString());
    txtLastName2.setText(tabelaPessoa.getValueAt(linha, 2).toString());
    txtNacionality2.setText(tabelaPessoa.getValueAt(linha, 3).toString());
    }//GEN-LAST:event_tabelaPessoaMouseClicked

